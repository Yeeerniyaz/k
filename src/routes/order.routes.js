import express from 'express';
import {
    getOrders,
    getOrderById,
    createOrder,
    updateOrder,
    deleteOrder
} from '../controllers/order.controller.js';

// Подключаем наши Enterprise-мидлвары для защиты роутов
import { protect, authorize } from '../middlewares/auth.middleware.js';

const router = express.Router();

// ==========================================
// 1. ПУБЛИЧНЫЕ МАРШРУТЫ (ОТКРЫТЫ ДЛЯ FRONTEND)
// ==========================================
// Создание нового заказа. Этот эндпоинт открыт для всех гостей сайта,
// которые пользуются калькулятором и отправляют заявку.
router.post('/', createOrder);

// ==========================================
// 2. ЗАЩИЩЕННЫЕ МАРШРУТЫ (CRM / ERP ПАНЕЛЬ)
// ==========================================
// Включаем обязательную проверку JWT-токена для всех роутов ниже.
// Только авторизованные сотрудники или B2B-клиенты могут пройти дальше.
router.use(protect);

// 🔥 SENIOR SECURITY: Ограничиваем доступ к просмотру всех заказов.
// Получить список всех заказов (для таблицы в админке) могут только сотрудники.
// Обычным клиентам просмотр чужих заказов строго запрещен.
router.get(
    '/',
    authorize('ADMIN', 'OWNER', 'MANAGER'),
    getOrders
);

// ==========================================
// 3. ОПЕРАЦИИ С КОНКРЕТНЫМ ЗАКАЗОМ (ПО ID)
// ==========================================
router.route('/:id')
    // Получить детали одного заказа (для открытия карточки заказа)
    // Доступ: сотрудники компании
    .get(
        authorize('ADMIN', 'OWNER', 'MANAGER'),
        getOrderById
    )

    // Обновить заказ (изменить статус, цену, назначить менеджера)
    // Доступ: все сотрудники. (Но контроллер внутри сам решит, какие поля можно менять)
    .put(
        authorize('ADMIN', 'OWNER', 'MANAGER'),
        updateOrder
    )

    // 🔥 SENIOR SECURITY: Безвозвратное удаление заказа.
    // Это критическая операция (влияет на финансовую статистику).
    // Менеджерам удаление ЗАПРЕЩЕНО. Они могут только поставить статус "CANCELED".
    // Удалять имеют право только Владелец бизнеса и Сисадмин.
    .delete(
        authorize('OWNER'),
        deleteOrder
    );

export default router;