import express from 'express';
import {
    getOrders,
    getOrderById,
    createOrder,
    updateOrder,
    deleteOrder
} from '../controllers/order.controller.js';
import { protect, authorize } from '../middlewares/auth.middleware.js';

const router = express.Router();

// ==========================================
// 1. ПУБЛИЧНЫЕ И СМЕШАННЫЕ МАРШРУТЫ
// ==========================================

// 🔥 SENIOR ARCHITECTURE: POST-запрос открыт для всех (без protect).
// Заявки должны падать с калькулятора (Home.jsx) даже от неавторизованных гостей.
// Если запрос придет от залогиненного B2B-клиента с токеном, 
// контроллер автоматически "подхватит" его и привяжет заказ к его аккаунту (clientId).
router.post('/', createOrder);

// ==========================================
// 2. ЗАЩИЩЕННЫЕ МАРШРУТЫ (CRM / ERP СИСТЕМА)
// ==========================================

// Включаем проверку JWT-токена для всех маршрутов ниже этой строки
router.use(protect);

// GET: Получение списка всех заказов для канбан-доски или таблицы менеджера.
// Доступ запрещен обычным клиентам (CLIENT), открыт только для персонала.
router.get(
    '/', 
    authorize('ADMIN', 'OWNER', 'MANAGER'), 
    getOrders
);

// ==========================================
// 3. ОПЕРАЦИИ С КОНКРЕТНЫМ ЗАКАЗОМ ПО ID
// ==========================================

router.route('/:id')
    // GET: Открыть карточку конкретного заказа со всеми расходами
    .get(
        authorize('ADMIN', 'OWNER', 'MANAGER'), 
        getOrderById
    )
    
    // PUT: Обновление заказа (смена статуса на "В работе", изменение цены, привязка затрат).
    // Менеджеры имеют право двигать сделки по воронке продаж.
    .put(
        authorize('ADMIN', 'OWNER', 'MANAGER'), 
        updateOrder
    )
    
    // DELETE: Безвозвратное удаление заказа из базы данных.
    // 🔥 SENIOR SECURITY FIX: Права урезаны. Менеджер (MANAGER) больше не может физически 
    // удалить заказ (во избежание махинаций). Менеджер должен перевести заказ в статус CANCELED.
    // Физическое удаление (DELETE) разрешено только Владельцу бизнеса и Системному Админу.
    .delete(
        authorize('ADMIN', 'OWNER'), 
        deleteOrder
    );

export default router;