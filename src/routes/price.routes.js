import express from 'express';
import {
    getPrices,
    createPrice,
    updatePrice,
    deletePrice
} from '../controllers/price.controller.js';

// Импортируем наши сеньорские middleware для защиты маршрутов
import { protect, authorize } from '../middlewares/auth.middleware.js';

const router = express.Router();

// ==========================================
// 1. ПУБЛИЧНЫЕ МАРШРУТЫ (ОТКРЫТЫ ДЛЯ ВСЕХ)
// ==========================================
// Получение прайс-листа должно быть доступно всем, 
// так как смарт-калькулятор на главной странице (Home.jsx) запрашивает эти данные без авторизации.
router.get('/', getPrices);

// ==========================================
// 2. ЗАЩИЩЕННЫЕ МАРШРУТЫ (ДЛЯ АДМИНКИ)
// ==========================================
// Применяем middleware 'protect'. Все запросы, идущие ниже этой строки,
// обязаны иметь валидный JWT токен в заголовках.
router.use(protect);

// Добавление новой услуги в прайс-лист (Доступно Менеджерам и Админам)
router.post('/', createPrice);

// ==========================================
// 3. ОПЕРАЦИИ С КОНКРЕТНОЙ ПОЗИЦИЕЙ
// ==========================================
router.route('/:id')
    // Редактирование цены, названия или единицы измерения
    .put(updatePrice)
    
    // Удаление услуги из прайс-листа
    // 🔥 СЕНЬОРСКАЯ ФИЧА: Удалять цены из базы может ТОЛЬКО администратор
    .delete(authorize('ADMIN'), deletePrice);

export default router;