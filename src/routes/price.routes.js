import express from 'express';
import {
    getPrices,
    getPriceById,
    createPrice,
    updatePrice,
    deletePrice
} from '../controllers/price.controller.js';

// Подключаем наши Enterprise-мидлвары для защиты роутов
import { protect, authorize } from '../middlewares/auth.middleware.js';

const router = express.Router();

// ==========================================
// 1. ПУБЛИЧНЫЕ МАРШРУТЫ (ОТКРЫТЫ ДЛЯ FRONTEND)
// ==========================================
// Этот эндпоинт используется калькулятором на главной странице.
// Он должен быть доступен всем гостям сайта без проверки токена.
router.get('/', getPrices);

// ==========================================
// 2. ЗАЩИЩЕННЫЕ МАРШРУТЫ (УПРАВЛЕНИЕ ERP)
// ==========================================
// Включаем обязательную проверку JWT-токена для всех роутов ниже
router.use(protect);

// 🔥 СОЗДАНИЕ НОВОЙ ПОЗИЦИИ В ПРАЙСЕ
// Защита: Только Владелец и Системный Администратор. 
// Рядовые менеджеры не могут добавлять "левые" услуги в базу.
router.post(
    '/', 
    authorize('ADMIN', 'OWNER'), 
    createPrice
);

// ==========================================
// 3. ОПЕРАЦИИ С КОНКРЕТНОЙ ПОЗИЦИЕЙ (ПО ID)
// ==========================================
router.route('/:id')
    // Получить данные конкретной услуги (например, для автозаполнения формы редактирования)
    // Доступ: Руководство + Менеджеры (им можно смотреть детали)
    .get(
        authorize('ADMIN', 'OWNER', 'MANAGER'), 
        getPriceById
    )
    
    // Обновить цену или название услуги
    // 🔥 SENIOR SECURITY: Финансовая безопасность. Изменять цены может ТОЛЬКО руководство.
    .put(
        authorize('ADMIN', 'OWNER'), 
        updatePrice
    )
    
    // Безвозвратно удалить услугу из прайс-листа
    // 🔥 SENIOR SECURITY: Удаление доступно только руководству.
    .delete(
        authorize('ADMIN', 'OWNER'), 
        deletePrice
    );

export default router;