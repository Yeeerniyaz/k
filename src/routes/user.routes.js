import express from 'express';
import {
    getAllUsers, // 🔥 SENIOR UPDATE: Добавлена новая функция для получения списка всех пользователей
    getUsers,    // Оставлено старое название для 100% обратной совместимости старого фронтенда
    createUser,
    updateUser,
    deleteUser
} from '../controllers/user.controller.js';

// Импортируем наши сеньорские middleware для безопасности
import { protect, authorize } from '../middlewares/auth.middleware.js';

const router = express.Router();

// ==========================================
// ГЛОБАЛЬНАЯ ЗАЩИТА МАРШРУТОВ (ENTERPRISE ERP)
// ==========================================
// Управление пользователями — это защищенная зона базы данных.
// Ни один маршрут здесь не должен быть доступен гостям с улицы (без токена).
router.use(protect);

// ==========================================
// 1. УПРАВЛЕНИЕ СПИСКОМ ПОЛЬЗОВАТЕЛЕЙ
// Endpoint: /api/users/
// ==========================================
router.route('/')
    // Получить всех пользователей (клиентов и персонал для таблицы в админке)
    // Доступ: Руководство и Менеджеры (менеджерам нужно видеть базу клиентов B2B)
    .get(
        authorize('ADMIN', 'OWNER', 'MANAGER'), 
        getAllUsers
    )
    
    // Создать нового пользователя (клиента или сотрудника) вручную из админки
    // 🔥 SENIOR SECURITY FIX: Рядовой менеджер не может создавать аккаунты (во избежание фрода).
    // Только Администратор или Владелец.
    .post(
        authorize('ADMIN', 'OWNER'), 
        createUser
    );

// ==========================================
// 2. ОПЕРАЦИИ С КОНКРЕТНЫМ ПОЛЬЗОВАТЕЛЕМ (ПО ID)
// Endpoint: /api/users/:id
// ==========================================
router.route('/:id')
    // Получить данные конкретного пользователя для просмотра его карточки
    // (Имя контроллера getUsers оставлено намеренно, чтобы не сломать ваши старые axios-запросы)
    .get(
        authorize('ADMIN', 'OWNER', 'MANAGER'), 
        getUsers
    )
    
    // Обновить профиль пользователя (смена роли, телефона, пароля, аватара)
    // Доступ: Только Руководство (изменение прав и ролей)
    .put(
        authorize('ADMIN', 'OWNER'), 
        updateUser
    )
    
    // Безвозвратно удалить пользователя из базы
    // Доступ: Только Руководство (действие записывается в AuditLog)
    .delete(
        authorize('ADMIN', 'OWNER'), 
        deleteUser
    );

export default router;