import express from 'express';
import {
    register,
    login,
    getMe,
    logout
} from '../controllers/auth.controller.js';

// Подключаем наши middleware для защиты маршрутов
import { protect, authorize } from '../middlewares/auth.middleware.js';

const router = express.Router();

// ==========================================
// 1. ОТКРЫТЫЕ МАРШРУТЫ (ДЛЯ ВСЕХ)
// ==========================================

// Авторизация в системе. Возвращает токен и данные профиля (включая аватар и телефон)
router.post('/login', login);

// Стандартная регистрация. Оставлена полностью открытой для обратной совместимости.
// (Если у вас на сайте клиенты могут сами создавать B2B-аккаунты, этот маршрут им необходим)
router.post('/register', register);

// ==========================================
// 2. ЗАЩИЩЕННЫЕ МАРШРУТЫ (ТОЛЬКО ДЛЯ АВТОРИЗОВАННЫХ)
// ==========================================

// 🔥 SENIOR PATTERN: Включаем проверку JWT-токена для всех маршрутов ниже этой строки.
// Если токена нет или он просрочен, до контроллеров запрос даже не дойдет (вернется 401).
router.use(protect);

// Получить данные своего профиля (используется на фронтенде при загрузке приложения / useEffect)
router.get('/me', getMe);

// Выход из системы. На стороне бэкенда фиксирует время выхода в AuditLog (для контроля рабочего времени)
router.post('/logout', logout);

// ==========================================
// 3. АДМИНИСТРАТИВНЫЕ МАРШРУТЫ (ENTERPRISE ERP) 🔥 НОВОЕ
// ==========================================

// 🔥 SENIOR SECURITY UPDATE: Безопасное создание сотрудников из админ-панели.
// Этот маршрут переиспользует мощную логику регистрации, но доступ к нему имеют 
// ТОЛЬКО Владелец (OWNER) и Системный Администратор (ADMIN). 
// Менеджер или Клиент не смогут создать себе коллегу.
router.post(
    '/create-staff',
    authorize('OWNER', 'ADMIN'),
    register
);

export default router;