import express from 'express';
import {
    getUsers,     // Старая функция для 100% совместимости
    getAllUsers,  // Новая расширенная Enterprise-функция (с фильтрацией ролей)
    createUser,
    updateUser,
    deleteUser
} from '../controllers/user.controller.js';

// Подключаем наши Enterprise-мидлвары для защиты роутов
import { protect, authorize } from '../middlewares/auth.middleware.js';

const router = express.Router();

// ==========================================
// ГЛОБАЛЬНАЯ ЗАЩИТА МАРШРУТОВ (ENTERPRISE ERP)
// ==========================================
// Управление пользователями — это строго конфиденциальная зона.
// Включаем обязательную проверку JWT-токена для ВСЕХ маршрутов ниже этой строки.
// Ни один неавторизованный гость не сможет получить список клиентов.
router.use(protect);

// 🔥 SENIOR SECURITY: Ограничиваем доступ по ролям.
// Доступ к модулю "Пользователи" имеют только Владелец и Системный Администратор.
// Рядовые менеджеры (MANAGER) не должны видеть базу всех клиентов компании 
// (чтобы не увести их к конкурентам) и не могут менять зарплаты/роли коллег.
router.use(authorize('OWNER', 'ADMIN'));

// ==========================================
// 1. ПОЛУЧЕНИЕ СПИСКА ПОЛЬЗОВАТЕЛЕЙ
// Endpoint: /api/users
// ==========================================
router.route('/')
    // Получить список всех пользователей системы.
    // Если фронтенд делает запрос GET /api/users?role=MANAGER, 
    // контроллер вернет только сотрудников.
    .get(getAllUsers)

    // Создать нового пользователя (менеджера, админа или B2B-клиента)
    // вручную через панель управления (вместо открытой регистрации)
    .post(createUser);

// ==========================================
// 2. ОПЕРАЦИИ С КОНКРЕТНЫМ ПОЛЬЗОВАТЕЛЕМ (ПО ID)
// Endpoint: /api/users/:id
// ==========================================
router.route('/:id')
    // Обновить данные пользователя (сменить роль, сбросить пароль, обновить email)
    // 🔥 SENIOR SECURITY: Контроллер должен проверять, чтобы обычный ADMIN 
    // не мог понизить в должности OWNER'а (защита от захвата управления).
    .put(updateUser)

    // Безвозвратно удалить аккаунт из базы данных.
    // Запишется в AuditLog. 
    .delete(deleteUser);

// 🔥 ОСТАВЛЕНО ДЛЯ 100% ОБРАТНОЙ СОВМЕСТИМОСТИ
// Если старый React-код делал запрос по нестандартному роуту (например, GET /api/users/list),
// этот эндпоинт перехватит его и не выдаст ошибку 404.
router.get('/list', getUsers);

export default router;