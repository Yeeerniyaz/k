import express from 'express';
import { 
    register, 
    login, 
    getMe, 
    logout 
} from '../controllers/auth.controller.js';

// Подключаем наши middleware для защиты маршрутов
import { protect, authorize } from '../middlewares/auth.middleware.js';

const router = express.Router();

// ==========================================
// 1. ОТКРЫТЫЕ МАРШРУТЫ (ДЛЯ ВСЕХ)
// ==========================================

// Авторизация в системе. Возвращает токен и данные профиля
router.post('/login', login);

// 🔥 SENIOR UPDATE: Публичная регистрация клиентов ВРЕМЕННО ОТКЛЮЧЕНА.
// Чтобы старый фронтенд не сломался (если там осталась кнопка), мы не удаляем роут полностью,
// а возвращаем заглушку с понятной ошибкой (Статус 403 Forbidden).
router.post('/register', (req, res) => {
    res.status(403).json({
        status: 'fail',
        message: 'Публичная регистрация временно закрыта. Обратитесь к администратору для создания аккаунта.'
    });
});

// ==========================================
// 2. ЗАЩИЩЕННЫЕ МАРШРУТЫ (ТОЛЬКО ДЛЯ АВТОРИЗОВАННЫХ)
// ==========================================
// Включаем проверку JWT-токена для всех маршрутов ниже.
router.use(protect);

// Получить данные своего профиля
router.get('/me', getMe);

// Выход из системы
router.post('/logout', logout);

// ==========================================
// 3. АДМИНИСТРАТИВНЫЕ МАРШРУТЫ (ENTERPRISE ERP)
// ==========================================

// Безопасное создание сотрудников И КЛИЕНТОВ из админ-панели.
// Теперь это единственный способ добавить нового пользователя в базу.
router.post(
    '/create-staff', 
    authorize('OWNER', 'ADMIN', 'MANAGER'), // Дал доступ менеджерам, чтобы они могли добавлять клиентов
    register
);

export default router;